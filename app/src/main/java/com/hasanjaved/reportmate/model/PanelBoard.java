package com.hasanjaved.reportmate.model;

public class PanelBoard {
    String testVoltage;
    String modelNo;
    String catalog;
    String amps;
    String voltage;

    public String getTestVoltage() {
        return testVoltage;
    }

    public void setTestVoltage(String testVoltage) {
        this.testVoltage = testVoltage;
    }

    public String getModelNo() {
        return modelNo;
    }

    public void setModelNo(String modelNo) {
        this.modelNo = modelNo;
    }

    public String getCatalog() {
        return catalog;
    }

    public void setCatalog(String catalog) {
        this.catalog = catalog;
    }

    public String getAmps() {
        return amps;
    }

    public void setAmps(String amps) {
        this.amps = amps;
    }

    public String getVoltage() {
        return voltage;
    }

    public void setVoltage(String voltage) {
        this.voltage = voltage;
    }
}
